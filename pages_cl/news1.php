            whereas the programming of data processing systems evolved into standardized routines and packages of enterprise resource planning (ERP) software. On the whole, these ERP systems have their origin from software that integrates information from different applications into one universal database. The linkage of its financial and human resource modules through one database is the most important distinction to the individually and proprietary developed 
            predecessors, which makes this software application both rigid and flexible.
            A Human Capital Management Solution, Human Resources Management System (HRMS) or Human 
            Resources Information System (HRIS), as it is commonly called is the crossing of HR systems and 
            processes with information technology. The wave of technological advancement has revolutionized 
            each and every space of life today, and HR in its entirety was not left untouched by it. What started off with a
             simple software to help improve the payroll processing of an organization, or a software to track the employee 
             work timings has grown to become the Human Resources systems that helps improve the process efficiency, reduces 
             the cost and time spent on mundane tasks and at the same time improved the overall experience of the employees 
             and the HR professionals. In short, as the role of Human Resources function evolved, HR technology systems also 
             changed the role they were playing.
            The function of human resources (HR) departments is administrative and common to all organizations.
             Organizations may have formalized selection, evaluation, and payroll processes. Management of "human capital" 
            progressed to an imperative and complex process. The HR function consists of tracking existing 
            employee data which traditionally includes personal histories, skills, capabilities, accomplishments and salary. 
            To reduce the manual workload of these administrative activities, organizations began to electronically 
            automate many of these processes by introducing specialized
             human resource management systems. HR executives rely on internal or external IT professionals 
             to develop and maintain an integrated HRMS. Before client–server architectures evolved in the late 1980s, 
             many HR automation processes were relegated to mainframe computers that could handle large amounts of data
              transactions. In consequence of the high capital investment necessary to buy or program proprietary software, 
              these internally developed HRMS were limited to organizations that possessed a large amount of capital. 
              The advent of client–server, application service provider, and software as a service (SaaS) or
               human resource management systems enabled higher administrative control of such systems. 
               Currently human resource management systems encompass:
