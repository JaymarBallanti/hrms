    <article><h1>Ifugao Satoyama Meister Trainees showcase research studies in RP-Japan Forum</h1>
        <p style="text-indent:40px; text-align:justify;">Empowered through the Ifugao Satoyama Meister Training Program, the trainees showcased their research studies through poster presentation during the RP-Japan Forum on January 26, 2015 at Hotel Supreme, Baguio City.
        The title of the forum is “Glocal Innovators: Capacity Building for Sustainable Development and Human Well-being Through GIAHS Twinning Program”.
        This was among the activities attended by the trainees prior to the culmination of the program for the first batch in March 2015.
        The program is implemented through cost-sharing of resources among IFSU, University of the Philippines (UP) – Open University, Kanazawa University of Japan, and the Provincial Local Government Unit (PLGU).
        IFSU provides facilities and human resources, technical expertise, extension and research funding of trainees. UP-OU provides human resources and technical expertise. Kanazawa University also provides technical expertise, human resources Officer, field trip exposure funding, and training cost funding with funding assistance from Japan International Cooperation Agency (JICA) – Hokuriko. The PLGU provides facilities and technical expertise.
        IFSU President Dr. Serafin L. Ngohayon said there’s a little twist on the usual training given by agencies.
        “In the Ifugao Satoyama Meister Program, each trainee is given a mentor to assist him or her in the conduct of his or her activities, making sure that he or she completes the training course. The mentors or workforce are IFSU employees,” he said.
        One of the end results of the training is the profitability of the outputs of some trainees like Jenelyn K. Limmong’s study labelled “Growth Performance and Profitability of Organically Raised Upgraded Large White Hog Breed”. Having proven that organically raised pigs could have impressive growth performance and profitability, her neighbors in Banaue adopted her method and are now spreading the influence.
        Trainee Maria P. Nayusan’s study labelled “Development of Instant Baby Food from Ifugao Heirloom Rice” is now heading towards the production, packaging and commercialization of baby food out of Tinawon (Tenogon) Rice.
        Ronald Chug-e’s study labelled “Rice-Loach: Increasing Productivity in Mayoyao Rice Terraces” is underway to determine methods so that rice-loach will not only be for family consumption but to make it profitable.
        These and other research outputs are in connection to the significance of the project to beneficiaries raised by Dr. Ngohayon as follows: 1) Additional knowledge about ecology and conservation, 2) Deeper appreciation and mainstreaming of the Globally Important Agricultural Heritage Systems (GIAHS) concept for the conservation of the Ifugao Rice Terraces (IRT), and 3) Acquisition of skills for self and community development.
        The forum went on with the presentation of researches that were subjected for panel discussions.
        Among the guests were Jovencio Dipia-o, the Executive Assistant of Ifugao Governor Denis B. Habawel, Professor Koji Nakamura of Kanazawa University, Yuki Kawada, Project Formulation Adviser of JICA Philippines, DOST-CAR Director Julius Caesar Sicat, and Gerardo Banawa of DA-CAR.
        Invited as lecturers were Dr. Maria Victoria O. Espaldon of UP Los Baῆos, Dr. Sylvano Mahiwo of UP Diliman, Melanie Subilla of MPSPC, and Sanny Cortez of the Office of Tourism, Mulanay, Quezon. 
